package com.secondClass;

public class ShorthanOperators {
	public static void main(String[] args) {
		
		int num=1000;
		num=num+1000; // num=num+1000
		System.out.println(num);
		// shortcut Sytout....syso+ctrl+space
		
		int num2=10;
		num2=num2-5;
		int num3=1000;
		num3+=1000;// num3 = num3+1000
		int num4=50;
		num4+=20;
		
		System.out.println(num4);
		
		num4-=30; // num4=70-30
		num4/=10; // num4=40/10
		System.out.println(num4);
		
		num4%=2; //num4=4%2
		System.out.println(num4);
		
		int num5=100;
		num5+=100;
		
		System.out.println(num5);
		
		int num6=100;
		num6-=67;
		System.out.println(num6);
		
		float cakePiece=11f;
		cakePiece/=4;
		System.out.println(cakePiece);
		
		cakePiece=25;
		cakePiece/=7;
		System.out.println(cakePiece);
				
		int cakePiece1=25;
		cakePiece1%=7;
		System.out.println(cakePiece1);
		
				
	}

}
