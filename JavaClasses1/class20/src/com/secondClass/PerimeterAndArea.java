package com.secondClass;

public class PerimeterAndArea {
	public static void main(String [] args) {
		
		int a=5 , b=8;
		int perim=2*a+2*b;
		int square=a*b;
		
		System.out.println("Perimeter of a rectangle with with " +a + " and height " + b + " is equal to " +perim);
		System.out.println("Area of a rectangle with with " +a + " and height " + b + " is equal to " +square);
		
		
	}

}
